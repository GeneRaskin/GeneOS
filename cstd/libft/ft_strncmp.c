#include "../include/libft.h"

int	ft_strncmp(const char *s1, const char *s2, uint32_t n)
{
	if (n == 0)
		return (0);
	while (*s1 && *s1 == *s2 && --n > 0)
	{
		s1++;
		s2++;
	}
	return ((uint8_t)*s1 - (uint8_t)*s2);
}
